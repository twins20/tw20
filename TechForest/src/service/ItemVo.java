package service;

public class ItemVo {
	
	private int itIdx;
	private int pIdx;
	private String itName;
	private int itPrice;
	private String contents;
	private int itTCnt;
	private int itSCnt;
	private int status;
	
	public int getItIdx() {
		return itIdx;
	}
	public void setItIdx(int itIdx) {
		this.itIdx = itIdx;
	}
	public int getpIdx() {
		return pIdx;
	}
	public void setpIdx(int pIdx) {
		this.pIdx = pIdx;
	}
	public String getItName() {
		return itName;
	}
	public void setItName(String itName) {
		this.itName = itName;
	}
	public int getItPrice() {
		return itPrice;
	}
	public void setItPrice(int itPrice) {
		this.itPrice = itPrice;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public int getItTCnt() {
		return itTCnt;
	}
	public void setItTCnt(int itTCnt) {
		this.itTCnt = itTCnt;
	}
	public int getItSCnt() {
		return itSCnt;
	}
	public void setItSCnt(int itSCnt) {
		this.itSCnt = itSCnt;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
